import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version',
};

const SYSTEM_PROMPT = `You are a friendly and knowledgeable sales and support assistant for Business Internet Express â€” a leading provider of business internet, voice, and connectivity solutions.

Your role:
- Answer questions about business internet plans, pricing, speeds, and availability
- Help customers understand the difference between fiber, cable, and fixed wireless options
- Assist with common support questions (billing, service issues, upgrades)
- Guide potential customers toward checking availability at their address
- Be proactive about suggesting relevant products and bundles
- If a customer has a complex issue you can't resolve, suggest they contact support directly or fill out the contact form

Key facts:
- We partner with major carriers including Spectrum, AT&T, Comcast, Verizon, Lumen, Frontier, and many more
- We offer dedicated internet, broadband, SD-WAN, voice/UCaaS, and managed WiFi
- Customers can check availability by entering their address on the website
- We serve businesses of all sizes from small offices to enterprise

Tone: Professional yet approachable. Be concise but thorough. Use simple language, avoid jargon unless the customer uses it first.

IMPORTANT: Keep responses concise (2-4 sentences for simple questions, up to a short paragraph for complex ones). Use bullet points for lists. Always end with a helpful next step or question when appropriate.`;

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');

    if (!LOVABLE_API_KEY) {
      return new Response(JSON.stringify({ error: 'AI service not configured' }), {
        status: 500,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      });
    }

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-3-flash-preview',
        messages: [
          { role: 'system', content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: 'Too many requests. Please try again in a moment.' }), {
          status: 429,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: 'Service temporarily unavailable.' }), {
          status: 402,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }
      const errorText = await response.text();
      console.error('AI gateway error:', response.status, errorText);
      return new Response(JSON.stringify({ error: 'AI service error' }), {
        status: 500,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, 'Content-Type': 'text/event-stream' },
    });
  } catch (error) {
    console.error('Chat error:', error);
    return new Response(JSON.stringify({ error: error.message || 'Unknown error' }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
